int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	while (i * i < nb)
		i++;
	return ((i * i) == nb ? i : 0);
}
